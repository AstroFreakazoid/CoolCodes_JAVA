    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        String cedula = this.txtCedula.getText();
        String nombre = this.txtNombre.getText();
        String telefono = this.txtTelefono.getText();
        String correo = this.txtCorreo.getText();

        Object[] row = {cedula, nombre, telefono, correo};
        
        DefaultTableModel model = (DefaultTableModel) this.tblClientes.getModel();        
        model.addRow(row);
        
        JOptionPane.showMessageDialog(null, "Cliente registrado exitosamente!");
        
        limpiarInputs();

    }//GEN-LAST:event_btnGuardarActionPerformed

    private void limpiarInputs(){
        
        this.txtCedula.setText("");
        this.txtNombre.setText("");
        this.txtTelefono.setText("");
        this.txtCorreo.setText("");
    
    }
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        DefaultTableModel tableModel = (DefaultTableModel) tblClientes.getModel();
        int rowIndex = tblClientes.getSelectedRow(); 
        
        tableModel.removeRow(rowIndex);
        
        txtCedula.setEnabled(true);

    }//GEN-LAST:event_btnEliminarActionPerformed
    
    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed

        limpiarInputs();
        txtCedula.setEnabled(true);

    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        DefaultTableModel tableModel = (DefaultTableModel) tblClientes.getModel();
        int rowIndex = tblClientes.getSelectedRow(); 
        
        tableModel.setValueAt(this.txtNombre.getText(), rowIndex, 1);
        tableModel.setValueAt(this.txtTelefono.getText(), rowIndex, 2);
        tableModel.setValueAt(this.txtCorreo.getText(), rowIndex, 3);        

    }//GEN-LAST:event_btnModificarActionPerformed

