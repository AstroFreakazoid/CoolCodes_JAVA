    private void activarListenerTabla() {
        
        tblClientes.getSelectionModel().addListSelectionListener(new ListSelectionListener(){
            public void valueChanged(ListSelectionEvent event) {
                if(tblClientes.getSelectedRow() != -1){
                    txtCedula.setEnabled(false);
                    txtCedula.setText(tblClientes.getValueAt(tblClientes.getSelectedRow(), 0).toString());
                    txtNombre.setText(tblClientes.getValueAt(tblClientes.getSelectedRow(), 1).toString());
                    txtTelefono.setText(tblClientes.getValueAt(tblClientes.getSelectedRow(), 2).toString());
                    txtCorreo.setText(tblClientes.getValueAt(tblClientes.getSelectedRow(), 3).toString());
                }else{
                    limpiarInputs();
                }
            }
        });
        
    }
